"""
import_history.py — Import sacct CSV dump into the Shovly historical SQLite database.

Usage:
    python3 import_history.py [data/sacct_raw.csv] [--db data/historical.db]

The CSV is pipe-delimited with NO header, generated by dump_history.sh:
    JobID|User|Submit|Start|End|ReqCPUS|ReqMem|ReqTRES|ElapsedRaw|TimelimitRaw|State|ClusterName

Costs are calculated using the same instance-matching logic as app.py; keep
the two catalogs in sync if you update instance pricing.
"""

